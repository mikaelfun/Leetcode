        m, n = len(obstacleGrid), len(obstacleGrid[0])
        if len(obstacleGrid) == 0 or obstacleGrid[m - 1][n - 1] == 1 or obstacleGrid[0][0] == 1:
            return 0
        while queue:
            temp = queue.pop(0)
            if temp[0] == m - 1 and temp[1] == n - 1:
                count+=1
